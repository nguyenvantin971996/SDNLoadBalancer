from mininet.net import Mininet
from mininet.node import Controller, RemoteController, OVSKernelSwitch
from mininet.cli import CLI
from mininet.log import setLogLevel
from mininet.link import TCLink

def topology():
	net = Mininet(controller=RemoteController, link=TCLink, switch=OVSKernelSwitch)

	c0 = net.addController('c0', controller=RemoteController, ip='127.0.0.1', port=6653)

	h1 = net.addHost('h1', mac='00:00:00:00:00:01', ip='10.0.0.1')
	h2 = net.addHost('h2', mac='00:00:00:00:00:02', ip='10.0.0.2')

	s1 = net.addSwitch('s1')
	s2 = net.addSwitch('s2')
	s3 = net.addSwitch('s3')
	s4 = net.addSwitch('s4')
	s5 = net.addSwitch('s5')
	s6 = net.addSwitch('s6')
	s7 = net.addSwitch('s7')
	s8 = net.addSwitch('s8')
	s9 = net.addSwitch('s9')
	s10 = net.addSwitch('s10')
	s11 = net.addSwitch('s11')
	s12 = net.addSwitch('s12')
	s13 = net.addSwitch('s13')
	s14 = net.addSwitch('s14')
	s15 = net.addSwitch('s15')
	s16 = net.addSwitch('s16')
	s17 = net.addSwitch('s17')
	s18 = net.addSwitch('s18')
	s19 = net.addSwitch('s19')
	s20 = net.addSwitch('s20')

	net.addLink(s1, s2, 1, 1, bw=100, delay='70ms', loss=0)
	net.addLink(s5, s1, 1, 2, bw=100, delay='30ms', loss=0)
	net.addLink(s4, s5, 1, 2, bw=100, delay='10ms', loss=0)
	net.addLink(s3, s4, 4, 2, bw=100, delay='40ms', loss=0)
	net.addLink(s2, s3, 2, 3, bw=100, delay='70ms', loss=0)
	net.addLink(s6, s7, 2, 2, bw=100, delay='70ms', loss=0)
	net.addLink(s10, s6, 1, 1, bw=100, delay='60ms', loss=0)
	net.addLink(s9, s10, 1, 2, bw=100, delay='90ms', loss=0)
	net.addLink(s8, s9, 2, 2, bw=100, delay='70ms', loss=0)
	net.addLink(s7, s8, 1, 1, bw=100, delay='60ms', loss=0)
	net.addLink(s12, s11, 2, 1, bw=100, delay='70ms', loss=0)
	net.addLink(s13, s12, 1, 1, bw=100, delay='70ms', loss=0)
	net.addLink(s14, s13, 1, 3, bw=100, delay='20ms', loss=0)
	net.addLink(s15, s14, 1, 2, bw=100, delay='10ms', loss=0)
	net.addLink(s11, s15, 3, 2, bw=100, delay='40ms', loss=0)
	net.addLink(s20, s19, 2, 2, bw=100, delay='30ms', loss=0)
	net.addLink(s16, s20, 1, 1, bw=100, delay='80ms', loss=0)
	net.addLink(s17, s16, 1, 2, bw=100, delay='30ms', loss=0)
	net.addLink(s18, s17, 2, 2, bw=100, delay='60ms', loss=0)
	net.addLink(s19, s18, 1, 1, bw=100, delay='70ms', loss=0)
	net.addLink(s16, s3, 3, 1, bw=100, delay='20ms', loss=0)
	net.addLink(s3, s20, 2, 3, bw=100, delay='80ms', loss=0)
	net.addLink(s11, s8, 2, 4, bw=100, delay='70ms', loss=0)
	net.addLink(s15, s8, 3, 3, bw=100, delay='80ms', loss=0)
	net.addLink(s1, h1, 3, 1, bw=100, delay='1ms', loss=0)
	net.addLink(s5, s6, 3, 3, bw=100, delay='10ms', loss=0)
	net.addLink(s7, s4, 3, 3, bw=100, delay='30ms', loss=0)
	net.addLink(s12, s19, 3, 3, bw=100, delay='30ms', loss=0)
	net.addLink(s13, s18, 2, 3, bw=100, delay='90ms', loss=0)
	net.addLink(h2, s13, 1, 4, bw=100, delay='1ms', loss=0)

	c0.start()
	s1.start([c0])
	s2.start([c0])
	s3.start([c0])
	s4.start([c0])
	s5.start([c0])
	s6.start([c0])
	s7.start([c0])
	s8.start([c0])
	s9.start([c0])
	s10.start([c0])
	s11.start([c0])
	s12.start([c0])
	s13.start([c0])
	s14.start([c0])
	s15.start([c0])
	s16.start([c0])
	s17.start([c0])
	s18.start([c0])
	s19.start([c0])
	s20.start([c0])
	net.build()

	
	CLI(net)
	net.stop()

if __name__ == '__main__':
	setLogLevel( 'info' )
	topology()

