	s14 = net.addSwitch('s14')
	s15 = net.addSwitch('s15')
	s16 = net.addSwitch('s16')
	s17 = net.addSwitch('s17')
	s18 = net.addSwitch('s18')
	s19 = net.addSwitch('s19')
	s20 = net.addSwitch('s20')

	net.addLink(s6, h1, 1, 1, bw=100, delay='1ms', loss=0)
	net.addLink(s7, s6, 1, 2, bw=100, delay='70ms', loss=0)
	net.addLink(s8, s7, 1, 2, bw=100, delay='60ms', loss=0)
	net.addLink(s9, s8, 1, 2, bw=100, delay='70ms', loss=0)
	net.addLink(s10, s9, 1, 2, bw=100, delay='40ms', loss=0)
	net.addLink(s5, s10, 1, 2, bw=100, delay='10ms', loss=0)
	net.addLink(s15, s5, 1, 2, bw=100, delay='80ms', loss=0)
	net.addLink(s20, s15, 1, 2, bw=100, delay='100ms', loss=0)
	net.addLink(s19, s20, 1, 2, bw=100, delay='60ms', loss=0)
	net.addLink(s18, s19, 1, 2, bw=100, delay='100ms', loss=0)
	net.addLink(s17, s18, 1, 2, bw=100, delay='100ms', loss=0)
	net.addLink(s16, s17, 1, 2, bw=100, delay='50ms', loss=0)
	net.addLink(s11, s16, 1, 2, bw=100, delay='30ms', loss=0)
	net.addLink(s1, s11, 1, 2, bw=100, delay='20ms', loss=0)
	net.addLink(s6, s1, 3, 2, bw=100, delay='30ms', loss=0)
	net.addLink(s2, s1, 1, 3, bw=100, delay='30ms', loss=0)
