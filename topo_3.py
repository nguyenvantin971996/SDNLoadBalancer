	net.addLink(s3, s2, 2, 2, bw=100, delay='40ms', loss=0)
	net.addLink(s14, s15, 1, 3, bw=100, delay='60ms', loss=0)
	net.addLink(s13, s14, 1, 2, bw=100, delay='10ms', loss=0)
	net.addLink(s12, s13, 1, 2, bw=100, delay='100ms', loss=0)
	net.addLink(s12, s11, 2, 3, bw=100, delay='60ms', loss=0)
